function print_text(x,y,cad){
 var i,aux;
 var maxCols = _map.length/gb_maxRows;
 var id;
 var cont=0;
 //var cad_log='';
 var offset_font = 3; //3 tiles a sumar
 aux = (y*maxCols)+x;
 //La fuente letra A empieza en tile3
 for (i=0;i<cad.length;i++){
  id = cad[i].toUpperCase().charCodeAt(0);  
  switch (id){
   case 46: id=27;break; //.
   case 47: id=26;break; //barra /
   case 48: id=14; break; //El numero 0
   case 49: id=33; break; //1
   case 50: id=25; break; //2    
   case 51: id=34; break; //3
   case 52: id=35; break; //4
   case 53: id=18; break; //5
   case 54: id=36; break; //6
   case 55: id=37; break; //7
   case 56: id=38; break; //8
   case 57: id=39; break; //9
   case 44: id=27; break; //la , por .
   default: id = id-65;; break; //resto letras
  }
  
  if ((id<0)||(id>80)){
   _map[aux+cont] = 57; //todo rojo
   cont++;
   _map[aux+cont] = 57;
   cont++;
  }
  else{
   //cad_log = cad_log+id+' ';
   _map[aux+cont] = (id*2)+offset_font;
   cont++;
   _map[aux+cont] = ((id*2)+1)+offset_font;
   cont++;
  }
 }
 //console.log(cad_log);
}

