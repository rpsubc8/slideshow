function DrawMapToScreen(factor_x,factor_y){
 var x,y,id; 
 var maxCols = _map.length/gb_maxRows;
 var maxTiles = _tiles.length/16; 
 var cont_aux;
 var aux,aux1,aux2;
 var aux_x,aux_y;
 var offset_x=5;
 var offset_y=10;
 var cad_log=''; 
 strokeWeight(1); 
 for (y=0;y<gb_maxRows;y++){
  for (x=0;x<maxCols;x++){   
   id = _map[(y*maxCols)+x];
   //id = id-3;
   if (id>=0){
    cont_aux = id*2; //first row tile  
    cad_log+=id+' ';
    for (i=0;i<8;i++){
     aux1 = _tiles[cont_aux];    //read row first 4 pixels	
	 aux2 = _tiles[cont_aux+1]; //read row end 4 pixels	
	 //cad_log+='c'+cont_aux+' '+aux1+' '+aux2+'|';	
	 //cad_log+=aux1+' '+aux2+'|';	
	 aux_x = (x*4)*factor_x;
	 aux_y = ((y*8)+i)*factor_y;
	 //0x03,0x01
	 aux = ((aux1>>4)&(0x0F));
	 //aux = GetArtefactColorGray(aux);
	 aux = GetArtefactColor(aux);
	 //cad_log+=' '+aux;
	 //stroke(aux);	 
	 stroke(aux);
	 fill(aux);
	 rect((offset_x+(aux_x)),offset_y+aux_y,factor_x,factor_y);
	 aux = (aux1 & 0x0F);
	 //aux = GetArtefactColorGray(aux);
	 //cad_log+=' '+aux;
	 //stroke(aux);	 
	 stroke(GetArtefactColor(aux));
	 fill(GetArtefactColor(aux));
	 rect((offset_x+(aux_x+(1*factor_x))),offset_y+aux_y,factor_x,factor_y);	 
	 aux = ((aux2>>4)&(0x0F));
	 //aux = GetArtefactColorGray(aux);
	 //cad_log+=' '+aux;
	 //stroke(aux);	 
	 stroke(GetArtefactColor(aux));
	 fill(GetArtefactColor(aux));
	 rect((offset_x+(aux_x+(2*factor_x))),offset_y+aux_y,factor_x,factor_y);	
	 aux = (aux2 & 0x0F);
	 //aux = GetArtefactColorGray(aux);	 
	 //cad_log+=' '+aux;
	 //stroke(aux);		 
	 stroke(GetArtefactColor(aux));
	 fill(GetArtefactColor(aux));
     rect((offset_x+(aux_x+(3*factor_x))),offset_y+aux_y,factor_x,factor_y);
	 cont_aux = cont_aux+ (maxTiles*2); //next row Tile   	
    }
   }
  }
 }
 //console.log(cad_log);
}





