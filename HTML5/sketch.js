function poll_gamepad(){
 //keyPressed(); 
 if (gb_gamepad_pressed === true){
  if (gb_pad_down === true){   
   //ScrollUp();
   gb_current_image++;
   gb_changed_display= true;
  }
  else
  {
   if (gb_pad_up === true){   
    //ScrollDown();
	gb_current_image--;
	gb_changed_display= true;
   }
   else{
	if (gb_pad_right === true){
	 //ScrollLeft();
	 gb_current_image++;
	 gb_changed_display= true;
	}
	else{   
	 if (gb_pad_left === true){	 
	  gb_current_image++;
	  gb_changed_display= true;
	 }		
	}
   }
  }
 }
 
 //Pad real
 let pads = navigator.getGamepads();
 let pad0 = pads[0]; 
 if (pad0){
  //console.log(pad0.axes[1]);
  //console.log(pad0.buttons);  
  if (pad0.axes[1]==-1){//y down
   gb_current_image++;
   gb_changed_display= true;
  }
  else{
   if (pad0.axes[1]==1){//up
    gb_current_image--;
    gb_changed_display= true;	   
   }
  }
  if (pad0.axes[0]==1){//x right
   gb_current_image++;
   gb_changed_display= true;
  }
  else{
   if (pad0.axes[0]==-1){//left
    gb_current_image--;
    gb_changed_display= true;	   
   }
  }
 }

  
 ResetPadPressed(); 
}

