function GetArtefactColor(aux){
 //Return RGB
 var aReturn=new Array(0,0,0);
 switch (aux){
  case 0: aReturn[0]=0;aReturn[1]=0;aReturn[2]=0; break;
  case 0x01: aReturn[0]=174;aReturn[1]=62;aReturn[2]=0; break;
  case 0x02: aReturn[0]=32;aReturn[1]=144;aReturn[2]=0; break;
  case 0x03: aReturn[0]=194;aReturn[1]=197;aReturn[2]=0; break;
  case 0x04: aReturn[0]=7;aReturn[1]=111;aReturn[2]=189; break;
  case 0x05: aReturn[0]=147;aReturn[1]=147;aReturn[2]=147; break;
  case 0x06: aReturn[0]=23;aReturn[1]=251;aReturn[2]=107; break;
  case 0x07: aReturn[0]=183;aReturn[1]=255;aReturn[2]=82; break;
  case 0x08: aReturn[0]=143;aReturn[1]=31;aReturn[2]=247; break;
  case 0x09: aReturn[0]=255;aReturn[1]=79;aReturn[2]=221; break;
  case 0x0A: aReturn[0]=163;aReturn[1]=163;aReturn[2]=163; break;
  case 0x0B: aReturn[0]=255;aReturn[1]=216;aReturn[2]=137; break;
  case 0x0C: aReturn[0]=132;aReturn[1]=129;aReturn[2]=255; break;
  case 0x0D: aReturn[0]=255;aReturn[1]=182;aReturn[2]=255; break;
  case 0x0E: aReturn[0]=152;aReturn[1]=255;aReturn[2]=255; break;
  case 0x0F: aReturn[0]=255;aReturn[1]=255;aReturn[2]=255; break;
  default: aReturn=0; break;
 }
 return aReturn;
}

